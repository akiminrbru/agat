const swiper3 = new Swiper(".swiper3", {
    // Optional parameters
    direction: "horizontal",
    loop: true,
    allowTouchMove: false,
    slidesPerView: 1,
    pagination: {
        el: ".swiper-pagination",
        // type: "fraction",
    },

    navigation: {
        nextEl: ".swiper-button-next",
        prevEl: ".swiper-button-prev",
    },
});

const swiperPrev3 = document.getElementById("swiperPrev3");
const swiperNext3 = document.getElementById("swiperNext3");

swiperPrev3.addEventListener("click", () => {
    swiper3.slidePrev();
});
swiperNext3.addEventListener("click", () => {
    swiper3.slideNext();
});

